import XCTest
@testable import KyotoTrip

class InfoCellViewDataTests: XCTestCase {
    
    var viewData: InfoCellViewData!
    
    override func setUp() {
    }
    
    func test_check_publishDateFormat() {
        let viewData = InfoCellViewData(
            title: "京都市考古資料館特別展示「光秀と京～入京から本能寺の変～」の会期延長について ",
            publishDate: "Fri, 05 Jun 2020 10:00:00 +0900",
            link: "https://www.city.kyoto.lg.jp/bunshi/page/0000270768.html"
        )

        XCTAssertEqual("2020/06/05 10:00:00", viewData.publishDateForCellView)
    }
    
    override func tearDown() {
    }
}
